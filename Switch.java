package conttrolstatement;

public class Switch {

	public static void main(String[] args) {
		String name="Shamsheera";
	
		switch(name){
		//Case statements
		case "Fowziya" : System.out.println("She likes Maths");
		break;
		case "subi": System.out.println("She likes English");
		break;
		case "Ayisha": System.out.println("She likes Science");
		break;
		//Default case statement
		default:System.out.println("Not likes English,Maths, or Science");
		}

	}

}
