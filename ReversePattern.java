package Activity3;

public class ReversePattern {

	public static void main(String[] args) {
		
		int row=4;
		for(int i=0;i<row;i++)
		{
			for(int k=i;k<row;k++)
			{
				System.out.print(" ");
			}
			for(int j=0;j<=i;j++)
			{
				System.out.print("*");
			}
			System.out.println();


	}
}
}
